package tiko.engine.gui;

/**
 * Defines dimensional objects.
 *
 * @author Jani Timonen
 * @version 1.0
 * @since 1.8
 */
public interface Dimensional {

    /**
     * Returns width of an object.
     *
     * @return width of an object.
     */
    int getWidth();

    /**
     * Returns height of an object.
     *
     * @return height of an object.
     */
    int getHeight();
}
